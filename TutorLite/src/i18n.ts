const DICTS: Record<Locale, Dict> = {
  en,
  "zh-cn": zhCn,
  "zh-tw": zhTw,
  ja
};

/** The raw locale tables, exposed for the i18n parity test. */
export const localeTables: Record<Locale, Dict> = DICTS;

/** Map an Obsidian language code (localStorage "language") to a known locale. */
export function resolveLocale(raw: string | null | undefined): Locale {
  const code = (raw ?? "").toLowerCase();
  if (code.startsWith("zh-tw") || code.startsWith("zh-hant")) return "zh-tw";
  if (code.startsWith("zh")) return "zh-cn";
  if (code.startsWith("ja")) return "ja";
  return "en";
}

const KNOWN: readonly Locale[] = ["en", "zh-cn", "zh-tw", "ja"];

let current: Locale = "en";

export function setLocale(raw: string | null | undefined): void {
  current = resolveLocale(raw);
}

/** The active locale (for code that must name the user's language, e.g. glosses). */
export function getLocale(): Locale {
  return current;
}

/**
 * Apply a language preference. "auto" (or anything unknown) resolves from
 * Obsidian's detected language; an explicit locale wins outright.
 */
export function setLanguage(
  preference: string | null | undefined,
  detected: string | null | undefined
): void {
  current =
    preference && (KNOWN as readonly string[]).includes(preference)
      ? (preference as Locale)
      : resolveLocale(detected);
}

/** Translate a key for the active locale, filling {placeholders} from vars. */
export function t(key: string, vars?: Record<string, string | number>): string {
  let value = DICTS[current][key] ?? en[key] ?? key;
  if (vars) {
    for (const [name, replacement] of Object.entries(vars)) {
      value = value.replace(`{${name}}`, String(replacement));
    }
  }
  return value;
}
